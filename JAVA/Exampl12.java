package com.ust.examples;
/*
 * switch cases
 * 
 */

 class Exampl12 {

	public static void main(String[] args) {
		char ch='b';
		switch(ch) {
		case 'd':
			System.out.println("case1");
			break;
		case 'b':
			System.out.println("case2");
			break;
			
		case 'x':
			System.out.println("case 3");
			break;
			
		case 'y':
			System.out.println("case 4");
             break;
			
		default:
			System.out.println("default");
		}
			
		}
		

	}


